import { NestFactory } from '@nestjs/core';
import { SwaggerModule, DocumentBuilder } from '@nestjs/swagger';
import { ValidationPipe } from '@nestjs/common';
import { AppModule } from './app.module';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);

  // Enable validation globally
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
    }),
  );

  // Swagger configuration
  const config = new DocumentBuilder()
    .setTitle('Water App API')
    .setDescription('Multi-tenant water application with role-based access control')
    .setVersion('1.0')
    .addTag('Authentication', 'Login, refresh token, and logout endpoints')
    .addTag('Companies', 'Company management (SuperAdmin only)')
    .addTag('Users', 'User management with role-based access')
    .addBearerAuth()
    .build();
  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api', app, document);

  await app.listen(process.env.PORT ?? 3000);
  console.log(`ðŸš€ Application is running on: http://localhost:${process.env.PORT ?? 3000}`);
  console.log(`ðŸ“š Swagger documentation: http://localhost:${process.env.PORT ?? 3000}/api`);
}
bootstrap();

